                    <h4 class="mb-3">Почему тип поля enum на уровне базы — зло</h4>


                    <div class="body-content"><p>Часто разработчики интересуются почему не рекомендуется использовать
                                                 тип поля <code>enum</code> в базе данных, и в этой статье мы рассмотрим
                                                 все плюсы и минусы данного типа.</p>
                        <p>Тип колонки <code>enum</code> используется для хранения данных, которые могут принимать
                           определённые значения из заранее определённого набора. Он обеспечивает ограничение значений,
                           которые может принимать колонка, и позволяет более строго контролировать данные. Это может
                           быть полезно для хранения статусов, категорий, типов или любых других значений, которые могут
                           быть заданы только из ограниченного набора вариантов.</p>
                        <p>Но это в теории. А что на практике? Давайте рассмотрим.</p>
                        <p>Допустим у нас есть таблица со списком платежей, содержащая колонку <code>status</code> со
                           следующими значениями:</p>
                        <pre><code class="sql hljs"><span class="hljs-keyword">CREATE</span> <span class="hljs-keyword">TABLE</span> `payments`(
  `id` <span class="hljs-type">INT</span> UNSIGNED <span class="hljs-keyword">NOT</span> <span
                                    class="hljs-keyword">NULL</span> AUTO_INCREMENT,
  `status` ENUM(<span class="hljs-string">'new'</span>, <span class="hljs-string">'progress'</span>, <span
                                    class="hljs-string">'done'</span>, <span class="hljs-string">'fauled'</span>) <span
                                    class="hljs-keyword">NOT</span> <span class="hljs-keyword">NULL</span>,
   KEY(`id`)
) CHARSET<span class="hljs-operator">=</span>utf8mb4 <span class="hljs-keyword">COLLATE</span><span
                                    class="hljs-operator">=</span>utf8mb4_unicode_ci;</code></pre>
                        <p>Через какое-то время была замечена грамматическая ошибка в слове "failed" и принято решение
                           её исправить.</p>
                        <p>Нюанс изменения enum поля в том, что при его редактировании сбрасываются значения колонки в
                            <code>null</code> для всех строк таблицы, а то и вовсе получим ошибку <code>Data truncated
                                                                                                        for column
                                                                                                        'status' at row
                                                                                                        3</code>. То
                           есть, чтобы корректно изменить <code>enum</code> поле, нужно куда-то сохранить данные. План
                           действий будет таков:</p>
                        <ol>
                            <li><p>Создать новую <code>enum</code> колонку с правильным набором данных;</p></li>
                            <li><p>Скопировать значение из старой колонки в новую и сразу применить исправление
                                   значения;</p></li>
                            <li><p>Удалить старую <code>enum</code> колонку;</p></li>
                            <li><p>Переименовать новую <code>enum</code> колонку.</p></li>
                        </ol>
                        <p>При использовании фреймворка Laravel это будет выглядеть следующим образом:</p>
                        <pre><code class="php hljs"><span class="hljs-keyword">use</span> <span class="hljs-title">Illuminate</span>\<span
                                    class="hljs-title">Database</span>\<span class="hljs-title">Migrations</span>\<span
                                    class="hljs-title">Migration</span>;
<span class="hljs-keyword">use</span> <span class="hljs-title">Illuminate</span>\<span
                                    class="hljs-title">Database</span>\<span class="hljs-title">Schema</span>\<span
                                    class="hljs-title">Blueprint</span>;
<span class="hljs-keyword">use</span> <span class="hljs-title">Illuminate</span>\<span class="hljs-title">Support</span>\<span
                                    class="hljs-title">Facades</span>\<span class="hljs-title">DB</span>;
<span class="hljs-keyword">use</span> <span class="hljs-title">Illuminate</span>\<span class="hljs-title">Support</span>\<span
                                    class="hljs-title">Facades</span>\<span class="hljs-title">Schema</span>;

<span class="hljs-keyword">return</span> <span class="hljs-keyword">new</span> <span class="hljs-class"><span
                                        class="hljs-keyword">class</span> <span
                                        class="hljs-keyword">extends</span> <span class="hljs-title">Migration</span>
</span>{
    <span class="hljs-keyword">public</span> <span class="hljs-function"><span
                                        class="hljs-keyword">function</span> <span class="hljs-title">up</span>(<span
                                        class="hljs-params"></span>): <span class="hljs-title">void</span>
    </span>{
        <span class="hljs-comment">// Создаём новую колонку</span>
        Schema::table(<span class="hljs-string">'payments'</span>, <span class="hljs-function"><span
                                        class="hljs-keyword">function</span> (<span class="hljs-params">Blueprint <span
                                            class="hljs-variable">$table</span></span>) </span>{
            <span class="hljs-variable">$table</span>-&gt;enum(<span class="hljs-string">'tmp_status'</span>, [<span
                                    class="hljs-string">'new'</span>, <span class="hljs-string">'progress'</span>, <span
                                    class="hljs-string">'done'</span>, <span class="hljs-string">'failed'</span>]);
        });

        <span class="hljs-comment">// Копируем значения из enum колонки в новую с корректировкой значения</span>
        DB::statement(<span class="hljs-string">'UPDATE payments SET tmp_status = (IF status = \'fauled\' THEN \'failed\' ELSE status END IF)'</span>);

        <span class="hljs-comment">// Удаляем старую колонку</span>
        Schema::table(<span class="hljs-string">'payments'</span>, <span class="hljs-function"><span
                                        class="hljs-keyword">function</span> (<span class="hljs-params">Blueprint <span
                                            class="hljs-variable">$table</span></span>) </span>{
            <span class="hljs-variable">$table</span>-&gt;dropColumn(<span class="hljs-string">'status'</span>);
        });

        <span class="hljs-comment">// Переименовываем колонку</span>
        Schema::table(<span class="hljs-string">'payments'</span>, <span class="hljs-function"><span
                                        class="hljs-keyword">function</span> (<span class="hljs-params">Blueprint <span
                                            class="hljs-variable">$table</span></span>) </span>{
            <span class="hljs-variable">$table</span>-&gt;renameColumn(<span
                                    class="hljs-string">'tmp_status'</span>, <span class="hljs-string">'status'</span>);
        });
    }
};</code></pre>
                        <p>На языке SQL эти действия будут выглядеть следующим образом:</p>
                        <pre><code class="sql hljs"><span class="hljs-keyword">ALTER</span> <span class="hljs-keyword">TABLE</span> `payments`
  <span class="hljs-keyword">ADD</span> <span class="hljs-keyword">COLUMN</span> `tmp_status` ENUM(<span
                                    class="hljs-string">'new'</span>, <span class="hljs-string">'progress'</span>, <span
                                    class="hljs-string">'done'</span>, <span class="hljs-string">'failed'</span>) <span
                                    class="hljs-keyword">NOT</span> <span class="hljs-keyword">NULL</span> AFTER `status`;

UPDATE `payments` <span class="hljs-keyword">SET</span> `tmp_status` <span class="hljs-operator">=</span> (
    IF `status` <span class="hljs-operator">=</span> <span class="hljs-string">'fauled'</span>
    <span class="hljs-keyword">THEN</span> <span class="hljs-string">'failed'</span>
    <span class="hljs-keyword">ELSE</span> `status` <span class="hljs-keyword">END</span> IF
);

<span class="hljs-keyword">ALTER</span> <span class="hljs-keyword">TABLE</span> `payments`
  <span class="hljs-keyword">DROP</span> <span class="hljs-keyword">COLUMN</span> `status`;

<span class="hljs-keyword">ALTER</span> <span class="hljs-keyword">TABLE</span> `payments`
  CHANGE `tmp_status` `status` ENUM(<span class="hljs-string">'new'</span>, <span class="hljs-string">'progress'</span>, <span
                                    class="hljs-string">'done'</span>, <span class="hljs-string">'failed'</span>) CHARSET utf8mb4 <span
                                    class="hljs-keyword">COLLATE</span> utf8mb4_unicode_ci <span class="hljs-keyword">NULL</span>;</code></pre>
                        <blockquote><p>При использовании фреймворка Laravel нельзя просто взять и применить метод
                                       "change" на поле типа <code>enum</code>.Это вызовет ошибку:</p>
                            <p>Unknown column type "enum" requested. Any Doctrine type that you use has to be registered
                               with \Doctrine\DBAL\Types\Type::addType()...</p>
                            <p>Для решения этой проблемы приходится применять следующий костыль перед применением
                               изменений:</p></blockquote>
                        <pre><code class="php hljs"><span class="hljs-keyword">protected</span> <span
                                    class="hljs-function"><span class="hljs-keyword">function</span> <span
                                        class="hljs-title">fixEnum</span>(<span class="hljs-params"></span>): <span
                                        class="hljs-title">void</span>
</span>{
    <span class="hljs-variable">$platform</span> = Schema::getConnection()-&gt;getDoctrineSchemaManager()-&gt;getDatabasePlatform();
    <span class="hljs-variable">$platform</span>-&gt;registerDoctrineTypeMapping(<span class="hljs-string">'enum'</span>, <span
                                    class="hljs-string">'string'</span>);
}</code></pre>
                        <p>И, кроме этого, всё равно нужно будет на стороне приложения обрабатывать значение либо как
                           константу класса, либо как <code>enum</code> класс. Например:</p>
                        <pre><code class="php hljs"><span class="hljs-class"><span
                                        class="hljs-keyword">class</span> <span class="hljs-title">Payment</span> <span
                                        class="hljs-keyword">extends</span> <span class="hljs-title">Model</span>
</span>{
    <span class="hljs-keyword">protected</span> <span class="hljs-variable">$casts</span> = [
        <span class="hljs-string">'status'</span> =&gt; StatusEnum::class,
    ];
}

<span class="hljs-class"><span class="hljs-keyword">enum</span> <span class="hljs-title">StatusEnum</span>: <span
        class="hljs-title">string</span>
</span>{
    <span class="hljs-keyword">case</span> <span class="hljs-keyword">New</span>      = <span
                                    class="hljs-string">'new'</span>;
    <span class="hljs-keyword">case</span> Progress = <span class="hljs-string">'progress'</span>;
    <span class="hljs-keyword">case</span> Done     = <span class="hljs-string">'done'</span>;
    <span class="hljs-keyword">case</span> Failed   = <span class="hljs-string">'failed'</span>;
}</code></pre>
                        <p>А теперь представьте, что как только эта задача выполнена, пришла новая - добавить новый
                           статус - <code>Refund</code>...</p>
                        <p>Альтернатива этой боли - отказ от полей БД типа <code>enum</code> в пользу
                            <code>integer</code>. В этом случае как при изменении названия, так и при добавлении нового
                           всё что нужно будет сделать - это изменить содержимое самого <code>enum</code> класса.
                           Например:</p>
                        <pre><code class="php hljs"><span class="hljs-comment">// До</span>
<span class="hljs-class"><span class="hljs-keyword">enum</span> <span class="hljs-title">StatusEnum</span>: <span
        class="hljs-title">int</span>
</span>{
    <span class="hljs-keyword">case</span> <span class="hljs-keyword">New</span>      = <span
                                    class="hljs-number">0</span>;
    <span class="hljs-keyword">case</span> Progress = <span class="hljs-number">1</span>;
    <span class="hljs-keyword">case</span> Done     = <span class="hljs-number">2</span>;
    <span class="hljs-keyword">case</span> Fauled   = <span class="hljs-number">3</span>;
}

<span class="hljs-comment">// После</span>
<span class="hljs-class"><span class="hljs-keyword">enum</span> <span class="hljs-title">StatusEnum</span>: <span
        class="hljs-title">int</span>
</span>{
    <span class="hljs-keyword">case</span> <span class="hljs-keyword">New</span>      = <span
                                    class="hljs-number">0</span>;
    <span class="hljs-keyword">case</span> Progress = <span class="hljs-number">1</span>;
    <span class="hljs-keyword">case</span> Done     = <span class="hljs-number">2</span>;
    <span class="hljs-keyword">case</span> Failed   = <span class="hljs-number">3</span>;
    <span class="hljs-keyword">case</span> Refund   = <span class="hljs-number">4</span>;
}</code></pre>
                        <p>И всё. Никакой боли и задача закрывается буквально за несколько секунд.</p>
                        <p>Именно поэтому многие разработчики отказываются от использования колонок типа
                            <code>enum</code> в базе данных сохраняя не только время, потраченное на задачу, но и нервы
                           и, в качестве приятного бонуса, шанс потерять данные сводится к нулю.</p>
                        <p>Также можно ответить на вопрос "почему integer, а не string": дело в том, что поля типа
                            <code>string</code> подвержены грамматическим ошибкам и всё что они дают - это удобство
                           чтения таких данных в одной конкретной таблице без использования SQL запросов с применением
                           JOIN функций. Но если в слове будет допущена ошибка или слово нужно будет заменить на другое
                           - придётся отправлять запрос в базу, чего не нужно делать при использовании целочисленных
                           значений.</p>
                        <p>Таким образом, тип полей <code>enum</code> в базе является иррациональным и бессмысленным
                           действием за исключением крайне редких и специфичных кейсов.</p>
                        <h2>Заключение</h2>
                        <p>При всём этом, необходимо всегда валидировать данные перед записью в базу даже если
                           используется тип колонки <code>enum</code>, иначе высока вероятность поймать 500-ю ошибку от
                           базы вместо корректной обработки.</p>
                        <p>В том же Laravel правило валидации реализуется очень просто:</p>
                        <pre><code class="php hljs"><span class="hljs-keyword">public</span> <span
                                    class="hljs-function"><span class="hljs-keyword">function</span> <span
                                        class="hljs-title">rules</span>(<span class="hljs-params"></span>): <span
                                        class="hljs-title">array</span>
</span>{
    <span class="hljs-keyword">return</span> [
        <span class="hljs-string">'status'</span> =&gt; [Rule::enum(StatusEnum::class)],
    ];
}</code></pre>
